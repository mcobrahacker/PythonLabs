# Описати рекурсивну функцію Fact (N) дійсного типу, яка обчислює значення
# факторіала
# N! = 1 · 2 · ... · N
# (N> 0 - параметр цілого типу). За допомогою цієї функції обчислити факторіали
# п'яти даних чисел.

def fact(n):
    if n == 1:
        return 1
    else:
        return fact(n-1) * n

n = int(input("Enter a number "))

print ("Result: ", fact(n))